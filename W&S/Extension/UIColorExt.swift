import UIKit

extension UIColor {
    class var topBgColor: UIColor {
        return UIColor(red:255/255, green:255/255, blue:255/255, alpha:1.0)
    }
    
    class var bottomBgColor: UIColor {
        return UIColor(red:190/255, green:192/255, blue:203/255, alpha:1.0)
    }
    
    class var labelBg: UIColor {
        return UIColor(red: 0.92, green: 0.92, blue: 0.92, alpha: 1.00)
    }
}
